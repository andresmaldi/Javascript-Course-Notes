// Introduction to Objects
// Objects are another data structure used to store information in a more organized and detailed way than arrays

// Objects use curly braces "{}" to be defined
// We also have Properties. These define specifically what each element is, to make it easier to be referenced
// Example:

const andres = {
  firstName: "Andres" /* firstName is a property of the object andres*/,
  lastName: "Maldonado",
  age: 27 /* The value of each property can be whatever you want */,
  job: "programmer",
  friends: [
    "Laura",
    "Ana",
    "Carolina",
  ] /* Objects can have arrays within them */,
};

// An important difference between arrays and objects is that in objects the order of elements is not important
