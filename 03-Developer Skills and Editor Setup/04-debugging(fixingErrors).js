/* Debugging (Fixing Errors)
Software Bug: Defect or problem in a computer program. Basically, any unexpected or unintended behavior of a computer program is a software bug.
Bugs are completely normal in software development. */

/* Debugging: Process of finding, fixing, and preventing bugs.
        1. Identify
            - Become aware that there is a bug, either during development (ideal) or during production (less ideal)
        2. Find
            - Isolate where exactly the bug is happening
            - If the code is too big  a debugger becomes necessary
        3. Fix
            - Replace the wrong solution with a correct solution
        4. Prevent
            - Search for the bug in similar code
            - Writing test using testing software
*/
