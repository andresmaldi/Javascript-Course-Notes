// Call stack
// This is where code is executed using an execution context.

// Heap
// This is an unstructured memory pool
// It stores all objects needed in a memory

// Computer Science Sidenote: Compilation vs. Interpretation
// Compilation: Entire code is converted into machine code (0a and 1s) at once, and written to a binary file that can be executed by a computer.
// Interpretation: An interpreter runs through the source code and executes it line by line, happening right before execution and not compiled beforehand.
// Just-in-time (JIT) Compilation: Entire code is converted into machine code at once, then executed immediately.

// Parsing: Reading the code
// Abstract Syntax Tree: Data structure where each line of code is broken into meaningful pieces to the language and organized in a "tree".

