// A High Level Overview of Javascript

// High Level
// In a low-level program, a developer has to manage resources manually.
// While in a high-level program, the developer does not, sin eveything happens automatically.

// Garbage Collected
// Javascript's engine contains an algorithm that cleans the memory from time to time

// Interpreted or Just-In-Time Compiled Language
// JAvascript works only with 0s and 1s.
// After everything is written in a javascript document, it is translated for computers or compiled.

// Multi-Paradigm
// A paradigm is an approach and mindset of structuring code, which will direct your coding style and technique.
// Types of paradigms include:
// 1. Procedural programming
// 2. Onject-oriented programming (OOP)
// 3. Functional programming (FP)

// Prototype-based object-oriented
// The prototype is like a blueprint from which elements in a document will inherit their characteristics

// First-class Functions
// In a language with first-class function, functions are treated as variables.

// Dyanmic
// Data types are not assigned to variables
// They are only known when the code is ran
// This means that it is a dynamically-typed language

// Single-threaded
// Concurrencymodel: how the javascript engine handles multiple tasks happening at the same time.
// Javascript runs in one single thread, so it can only do one thing at a time.

// Non-blocking event loop
// If there is a long-running task, an event loop will take it, execute it in the background and put it back in the main thread once finished.
